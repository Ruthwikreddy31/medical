export function addLocalizedGuidance(response, language) {
  const dictionary = TRANSLATIONS[language];
  if (!dictionary) {
    return response;
  }

  const urgencyKey = {
    Emergency: "emergency",
    Urgent: "urgent",
    Moderate: "moderate",
    "Low Risk": "low"
  }[response.risk_assessment.urgency_level];

  return {
    ...response,
    localized_guidance: {
      language,
      primary_action: dictionary[urgencyKey],
      safety_disclaimer: dictionary.disclaimer
    }
  };
}
